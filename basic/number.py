# 정수형
a = 123
print(a)
a = -178
print(a)
a = 0
print(a)

# 실수형
a = 1.2
print(a)
a = -3.45
print(a)

# 컴퓨터식 지수 표현 방식
a = 4.23E10
print(a)
a = 4.24e-10
print(a)

# 복소수
a = 1 + 2j
print(a)
b = 3 - 4j
print(b)

# 복소수.real
a = 1 + 2j
print(a)
print(a.real)

# 복소수.imag
a = 1 + 2j
print(a)
print(a.imag)

# 복소수.conjugate
a = 1 + 2j
print(a.conjugate())
print(abs(a))

# 사칙연산
a = 3
b = 4
print(a + b)
print(a * b)
print(a / b)

# x의 y제곱을 나타내는 **연산자

a = 3
b = 4
print(a ** b)

